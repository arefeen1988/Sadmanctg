package looping;

public class WhileLoop2 {

	public static void main(String[] args) {

		// Write a program in Java to display the multiplication table of (eight)8. Use
		// While loop.

		int num = 8;

		int i = 1;
		while (i <= 10) {

			System.out.println(num + "X" + i + "=" + num * i);
			i++;
		}

	}

}
