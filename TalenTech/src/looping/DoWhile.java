package looping;

public class DoWhile {

	public static void main(String[] args) {
		
		//Write a Do-While loop to find all the odd numbers between 15 through 75. Print output in separate lines with proper format (Just do not print the numbers)


		int i = 15;

		do {
			System.out.println(i);
			i = i + 2;
		} while (i <= 75);

	}

}
